#          **	Возведение в степень
#          %	Остаток от деления
#          //	Целочисленное деление
print(123 ** 2)
print(123 % 2)
print(123 // 2)


# finding the first digit of 4 digit number
print(1234 // 1000)
# finding the second digit of 4 digit number
print(1234 // 100 % 10)
# finding the third digit of 4 digit number
print(1234 // 10 % 10)
# finding the last digit of 4 digit number
print(1234 % 10)